     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $this->validate($request,[
            'name'  => ['required'],
            'phone_number'  => ['required'],
            'email'  => ['required'],
            'address'  => ['required'],
            'gender'  => ['required'],
        ]);

        members::create($request->all());

        return redirect('member');
    }

